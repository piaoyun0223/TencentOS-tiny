int fputc(int ch, FILE *f)
{
    if (ch == '\n') {
        HAL_UART_Transmit(&huart4, (void *)"\r", 1, 30000);
    }
    HAL_UART_Transmit(&huart4, (uint8_t *)&ch, 1, 0xFFFF);
    return ch;

}
int _write(int fd, char *ptr, int len)
{
    (void)HAL_UART_Transmit(&huart4, (uint8_t *)ptr, len, 0xFFFF);
    return len;
}

int fgetc(FILE *f)
{
  uint8_t ch = 0;
  HAL_UART_Receive(&huart4, &ch, 1,30000);
  return ch;
}



